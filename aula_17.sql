-- FUNÇÕES MATEMÁTICAS COMUNS: ARREDONDAMENTOS NÃO TÃI CORRIQUEIROS

-- FLOOR = arredonda para baixo
SELECT FLOOR(9.563);

-- CEIL= arredonda para cima
SELECT CEIL(9.563);

-- TRUNCATE
SELECT TRUNCATE(9.563, 2);

-- ABS = absoluto, sem sinais de negativo e ou positivo
SELECT ABS(-243.5);

-- MOD = resto da divisão
SELECT MOD(3, 2);

-- POW = potência
SELECT POW(4,2);

-- SQRT = raiz quadrada
SELECT SQRT(64);

-- RAND = SORTEIO
SELECT RAND();

-- GREATEST = qual o maior número
SELECT GREATEST(3, 12, 34, 8, 25);